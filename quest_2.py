# 2) Discuta a importância das anotações de tipo (type hints) na linguagem Python e explique como elas podem ajudar no desenvolvimento de código.

"As type hints são importantes porque aumentam a segurança do código (via análise estática), melhoram drasticamente a manutenibilidade e a comunicação entre desenvolvedores, "
"e potencializam as ferramentas de autocompletar das IDEs, tornando o desenvolvimento mais rápido e menos propenso a bugs bobos de tipagem."
